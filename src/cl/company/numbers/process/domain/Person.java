package cl.company.numbers.process.domain;

public class Person {

	// atributos de la clase
	private String name;
	private String age;
	private String rut;
	
	// constructor
	public Person(){
		
	}

	// métodos
	
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getAge() {
		return age;
	}

	public void setAge(String age) {
		this.age = age;
	}

	public String getRut() {
		return rut;
	}

	public void setRut(String rut) {
		this.rut = rut;
	}
	
	
}
